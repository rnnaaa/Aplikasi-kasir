private void loadKategoriBarang() {
    try {
        KategoriBPK.removeAllItems();
        KategoriBPK.addItem("-- Pilih Kategori --");
        // Tambah kategori default manual
        KategoriBPK.addItem("Minuman");
        KategoriBPK.addItem("Eskrim");
        KategoriBPK.addItem("Makanan");
        KategoriBPK.addItem("Snack");
        KategoriBPK.addItem("Bahan Pokok");
        KategoriBPK.addItem("Bumbu Dapur");
        KategoriBPK.addItem("Perawatan Diri");
        KategoriBPK.addItem("Alat Rumah Tangga");

        // Ambil kategori unik dari barang_jual (jika ada input lain)
        Connection conn = Koneksi.getKoneksi();
        String sql = "SELECT DISTINCT kategori_barang FROM barang_jual";
        PreparedStatement ps = conn.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            String kategoriDB = rs.getString("kategori_barang");

            // Hindari duplikat (cek apakah sudah ada di combobox)
            boolean sudahAda = false;
            for (int i = 0; i < KategoriBPK.getItemCount(); i++) {
                if (KategoriBPK.getItemAt(i).equalsIgnoreCase(kategoriDB)) {
                    sudahAda = true;
                    break;
                }
            }

            if (!sudahAda) {
                KategoriBPK.addItem(kategoriDB);
            }
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Gagal load kategori: " + e.getMessage());
    }
}



