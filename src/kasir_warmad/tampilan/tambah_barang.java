    private String generateIDBarang() {
    String prefix = "BRNGBR25";
    String newID = "";

    try {
        Connection conn = Koneksi.getKoneksi();
        String sql = "SELECT id_barang_jual FROM barang_jual ORDER BY id_barang_jual DESC LIMIT 1";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        if (rs.next()) {
            String lastID = rs.getString("id_barang_jual"); // contoh: "BRNGBR25001"
            int lastNum = Integer.parseInt(lastID.substring(prefix.length())); // ambil "001"
            newID = prefix + String.format("%03d", lastNum + 1);
        } else {
            newID = prefix + "001";
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Gagal generate ID: " + e.getMessage());
    }

    return newID;
}

